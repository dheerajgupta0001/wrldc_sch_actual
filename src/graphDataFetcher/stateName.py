def stateNameData(stateName: str):
    """
    Args:
        stateName (str): sem state code for graph plotting
    Returns:
        Name of the state for graph plotting!!!
    """
    if stateName == "DNH":
        return "दादरा नगर हवेली"
    elif stateName == "DD":
        return "दमन दीव"
    elif stateName == "GO":
        return "गोवा"
    elif stateName == "CS":
        return "छत्तीसगढ"
    elif stateName == "MP":
        return "मध्य प्रदेश"
    elif stateName == "GU":
        return "गुजरात"
    elif stateName == "MH":
        return "महाराष्ट्र"