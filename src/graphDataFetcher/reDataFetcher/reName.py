def reDisplayNameData(stateName: str):
    """
    Args:
        stateName (str): sem state code for graph plotting
    Returns:
        Name of the state for graph plotting!!!
    """
    if stateName == "AM-91":
        return "एकमे सोलर"
    elif stateName == "MA-91":
        return "महेंद्र सोलर"
    elif stateName == "AR-91":
        return "अरिन्सन सोलर"
    if stateName == "OS-91":
        return "ओस्ट्रो पवन"
    elif stateName == "RE-91":
        return "भुवद पवन"
    elif stateName == "GI-91":
        return "वडवा पवन"
    elif stateName == "GI-94":
        return "नारनपार पवन"
    elif stateName == "IX-91":
        return "दयापार पवन"
    elif stateName == "AG-91":
        return "रताड़िया पवन"
    elif stateName == "AF-91":
        return "अल्फानारी पवन"
    elif stateName == "GH-91":
        return "गढ़सीसा पवन"